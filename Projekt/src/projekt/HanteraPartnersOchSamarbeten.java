    public void listaDelaktigaHandlaggare(){
try{
     ArrayList<String> anstallda =  idb.fetchColumn("SELECT concat('AID: ',aid,'  ', fornamn,' ',efternamn) FROM anstalld where aid in(Select aid from ans_proj where pid = "+aktuelltPID+") and aid in(select aid from handlaggare)");
     DefaultListModel<String> overforingsLista = new DefaultListModel<>();
            for(String a : anstallda){
                overforingsLista.addElement(a); }
            listaDelaktigaHandlaggare.setModel(overforingsLista);
}catch(InfException ex){
    System.out.println(ex.getMessage());
}
    }
       public void listaTillgangligaHandlaggare(){
try{
     ArrayList<String> anstallda =  idb.fetchColumn("SELECT concat('AID: ',aid,'  ', fornamn,' ',efternamn) FROM anstalld where aid not in(Select aid from ans_proj where pid = "+aktuelltPID+") and aid in(select aid from handlaggare)");
     DefaultListModel<String> overforingsLista = new DefaultListModel<>();
            for(String a : anstallda){
                overforingsLista.addElement(a); }
            listaTillgangligaHandlaggare.setModel(overforingsLista);
}catch(InfException ex){
    System.out.println(ex.getMessage());
}
    }
               public void listaTillgangligaPartners(){
try{
     ArrayList<String> partners =  idb.fetchColumn("SELECT concat('PID: ',pid,'  ', namn) FROM partner where pid not in(Select partner_pid from projekt_partner where pid ="+aktuelltPID+")");
     DefaultListModel<String> overforingsLista = new DefaultListModel<>();
            for(String a : partners){
                overforingsLista.addElement(a); }
            listaTillgangligaPartners.setModel(overforingsLista);
}catch(InfException ex){
    System.out.println(ex.getMessage());
}
    }
